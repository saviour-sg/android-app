package com.socgen.saviour;

public class Stats {
    private String newRequests;
    private String unitsDonated;
    private String activeDonors;

    public String getNewRequests() {
        return newRequests;
    }

    public void setNewRequests(String newRequests) {
        this.newRequests = newRequests;
    }

    public String getUnitsDonated() {
        return unitsDonated;
    }

    public void setUnitsDonated(String unitsDonated) {
        this.unitsDonated = unitsDonated;
    }

    public String getActiveDonors() {
        return activeDonors;
    }

    public void setActiveDonors(String activeDonors) {
        this.activeDonors = activeDonors;
    }
}
